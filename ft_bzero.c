#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*wiz;

	i = 0;
	wiz = (unsigned char *)s;
	while (i < n)
	{
		wiz[i] = 0;
		i++;
	}
	s = wiz;
}
